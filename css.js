/*Animation bouton HTML, CSS     1*/
let btnHtml1 = document.getElementById("button-html-1");
let codeHtml1 = document.getElementById("code-html-1");
let btnCss1 = document.getElementById("button-css-1");
let codeCss1 = document.getElementById("code-css-1");

codeCss1.style.display = "none";
btnCss1.onclick = afficher1;
function afficher1 () {
    codeCss1.style.display = "flex";
    codeHtml1.style.display = "none";
}

btnHtml1.onclick = cacher1;
function cacher1 () {
    codeHtml1.style.display = "flex";
    codeCss1.style.display = "none";
}

